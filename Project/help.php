		<p><b>1. Giới thiệu chung</b></p>
		<p>Việc đăng ký học của sinh viên là một việc bắt buộc và rất quan trọng trước khi bắt đầu mỗi học kỳ, xác nhận quyền lợi và trách nhiệm của sinh viên trong nhiệm vụ học tập của mình. Sinh viên không đăng ký học tại học kỳ chính thì sẽ bị buộc thôi học</p>
		<p>Nhà trường sẽ thông báo thời gian đăng ký học qua tuần công dân đầu khóa (đối với học kỳ đầu tiên khi sinh viên vừa nhập học); qua email cá nhân của sinh viên và website Đăng ký học trước các đợt đăng ký học ở các học kỳ sau đó. Việc đăng ký học được thực hiện qua mạng Internet, xác thực qua tài khoản của sinh viên tại website Đăng ký học (<a href="login.php">http://</a>)</p>
		<p>Sinh viên căn cứ vào Thời khóa biểu dự kiến được gửi qua email (mỗi sinh viên sẽ được cung cấp một email – thư điện tử để sử dụng trong quá trình học tập tại trường) và website Đăng ký học để lên kế hoạch học tập phù hợp cho bản thân.</p>
		<p>Nhà trường khuyến cáo sinh viên xây dựng cho mình các phương án đăng ký học khác nhau phù hợp với thời gian biểu của mình trước khi đi đăng ký học (lý do là có thể những lớp học phần sv lựa chọn đã đầy thì có thể chuyển sang nhóm khác). Các vấn đề nảy sinh trong vấn đề lựa chọn các phương án đăng ký phù hợp, các bạn sinh viên cần trao đổi với Cố vấn học tập (Cố vấn học tập kiêm Giáo viên chủ nhiệm là những thầy cô giáo được phân công quản lý lớp học theo từng Khoa) của lớp mình.</p>
		<p>Thông báo đăng ký học, đăng ký thi lại sẽ được gửi tới các bạn sinh viên qua email và website đăng ký học. Thông báo này thông thường sẽ được gửi trước khi đăng ký khoảng 1 – 2 tuần. Thời gian đăng ký sẽ diễn ra trước khi bắt đầu đợt học hoặc đợt thi 1 – 2 tuần. Như vậy, trong khoảng thời gian trước học kỳ mới hoặc đợt thi khoảng 2 – 4 tuần, sinh viên cần phải liên tục kiểm tra thông tin để nắm được kế hoạch mà nhà trường đưa ra.</p>
		<p>Sinh viên căn cứ vào các phương án đăng ký học của mình để đi đăng ký học theo thông báo về thời gian đăng ký học của nhà trường.</p>
		<p><b>2. Tài khoản đăng ký học</b></p>
		<p>Mỗi sinh viên khi vào nhập học vào trường được nhà trường cung cấp cho một Mã sinh viên. Mã sinh viên này là cố định trong suốt quá trình học tập của sinh viên tại trường, mọi quá trình đăng ký học và các vấn đề liên quan đến học tập, thi cử, xử lý học vụ, điểm thi, học phí… đều liên quan tới mã sinh viên này.</p>
		<p>Tài khoản tại website đăng ký học (sau đây gọi là Tài khoản ĐKH) cũng chính là mã sinh viên dung để đăng nhập vào Hệ thống phần mềm Đăng ký học trên website Đăng ký học của nhà trường. Mật khẩu mặc định cho lần đăng nhập đầu tiên sẽ chính là mã sinh viên.</p>
		<p>Cấu trúc của mã sinh viên như sau: <b>aabcccdddd</b> trong đó:</p>
		- <b>aa</b> là 2 số cuối của năm nhập học (ví dụ năm 2012 thì aa là 12)</p>
		- <b>b</b> là mã số thể hiện trình độ đào tạo (1 – cao đẳng hệ chính quy; 2 – liên thông hoàn chỉnh kiến thức, 3 – đại học hệ vừa làm vừa học, 5 – đại học hệ chính quy)</p>
		- <b>ccc</b> là mã ngành đào tạo được quy định tại bảng dưới đây</p>
		- <b>dddd</b> là số thứ tự hồ sơ nhập học của sinh viên.</p>
